package pe.albatross.zelpers.miscelanea;

import java.util.List;

public class ListsInspector {

    private List listDB;
    private List listForm;
    private List newList;
    private List deadList;
    private List oldListDB;
    private List oldListForm;

    public List getListDB() {
        return listDB;
    }

    public void setListDB(List listDB) {
        this.listDB = listDB;
    }

    public List getListForm() {
        return listForm;
    }

    public void setListForm(List listForm) {
        this.listForm = listForm;
    }

    public List getNewList() {
        return newList;
    }

    public void setNewList(List newList) {
        this.newList = newList;
    }

    public List getDeadList() {
        return deadList;
    }

    public void setDeadList(List deadList) {
        this.deadList = deadList;
    }

    public List getOldListDB() {
        return oldListDB;
    }

    public void setOldListDB(List oldListDB) {
        this.oldListDB = oldListDB;
    }

    public List getOldListForm() {
        return oldListForm;
    }

    public void setOldListForm(List oldListForm) {
        this.oldListForm = oldListForm;
    }

}
